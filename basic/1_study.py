def cal(n):
    if n > 500:
        b = n % 500*670.6 + n % 400*417.7+n % 300 * \
            280.6+n % 200*187.9 + n % 100*125.9 + 100*60.7
    elif n > 400:
        b = n % 400*417.7+n % 300*280.6+n % 200*187.9 + n % 100*125.9 + 100*60.7
    elif n > 300:
        b = n % 300*280.6+n % 200*187.9 + n % 100*125.9 + 100*60.7
    elif n > 200:
        b = n % 200*187.9 + n % 100*125.9 + 100*60.7
    elif n > 100:
        b = n % 100*125.9 + 100*60.7
    else:
        b = n*60.7

    print('=============================')
    print(f'{n}kWh의 전기요금은 {b}원 입니다.')
    print('=============================')


